<h2>Vida y Muerte</h2>
<p>
	<p>La vida no puede existir sin la muerte, por eso estoy aquí. Mi nombre es Mictlantecuhtli y dentro de la mitología mexica soy el señor de la oscuridad. En mi reino, llamado el Mictlán, acojo a todos los humanos que mueren de forma natural.

Fui creado por los dioses Huitzilopochtli y Quetzalcóatl en el Omeyocan, un lugar equivalente al cielo. Ellos pensaron que para valorar la vida, había que crear al dios de la muerte. ¿Cómo se puede amar la luz sin conocer la sombra? Por eso, sólo por eso estoy aquí.

Mi nombre se pronuncia en náhuatl. Está compuesto por dos palabras: Mictlán, mansión de los muertos y Tecutli, señor. Y se traduce como “el señor de la mansión de los muertos”.

Soy quien rige sobre la muerte de los seres humanos, pero a su vez soy dador de vida. ¿Por qué? Un día Quetzalcóatl bajó al inframundo y depositó su semen sobre unos huesos molidos que dieron vida al hombre.

Desde entonces custodio esos restos y todos me relacionan con la semilla de la vida. En algunos códices fui representado precediendo nacimiento y decepción. Algunos antropólogos lo han planteado como la vida y la muerte en una unidad.

En Mesoamérica, un territorio que fue tan inmerso, hubo varios dialectos. Por eso fui nombrado de diferentes formas, entre ellas: Ixtupec, que significa rostro quebrado; Sextepehua, esparcidor de cenizas y Tzontemoc, el que baja de cabeza.

¿Cómo es esto de bajar la cabeza? El Mictlán se encuentra debajo de la tierra y para accederlo las personas tienen que desplazarse hacia abajo y la forma más rápida es arrojarse de cabeza.

Cuando los españoles llegaron a nuestra tierra aparecieron misioneros que tradujeron al Mictlán como infierno y mi nombre como el diablo. Sólo podían imaginarse el mundo a través de su religión, pero éste fue su gran error.

El Mictlán no es un lugar de tinieblas, ni un lugar de castigo, simplemente es la morada de los muertos, de los descarnados. Incluso, cuando el Sol desaparece en el horizonte se dirige a mi hogar y es cuando los muertos se levantan de su sueño.

Les cuento que mi reino tiene nueve niveles. Las almas tenían que pasar por cada uno hasta llegar al último nivel, pero ustedes no lo sabrán hasta que llegue su día.
<p/>
<h1>Los niveles son:</h1>
<li>1.- Apanoayan: Todos los fallecidos deben acceder por un río donde se encuentra un perro y los ayuda a pasar nadando a cuestas.
</li>
<li>2.- Tepeme Monamictia: Lugar donde se encuentran dos montañas que chocan siempre una contra la otra.
</li>
<li>3.- Iztepetl: Significa cerro de obsidiana y como su nombre lo dice es un cerro erizado de cuchillos de pedernal.
</li>
<li>4.- Cehuecayan: Lugar donde hiela, se trata de otro lugar que deben pasar las almas.
</li>
<li>5.- Itzehecáyan: Sitio donde sopla el viento de obsidiana; es decir, que corta como cuchillo de pedernal.
</li>
<li>6.- Teocoylehualoyan: Espacio donde aparece un jaguar y devora el corazón del difunto.
</li>
<li>7.- Apanhuiayo: Es un lago de agua negra donde se encuentra una lagartija llamada Xochitonal e intenta detener el paso del difunto.
</li>
<li>8.- Chiconauapan: Este es el último sitio para llegar al Mictlán. El fallecido llega a la orilla de un río.
</li>
<li>9.- Por último, el alma ingresa a su destino: el Mictlán, donde está mi dulce hogar.
</li>
<h3>Campo magnético</h3>
<p>Las leyes de la física que conocemos hoy en día sostienen que el núcleo de la Tierra genera su campo magnético.
Las hermosas auroras boreales se producen cuando el viento solar choca con el campo magnético de la Tierra.<p>

<p>En un planeta plano, según los defensores de este modelo, este campo no existe.
Siendo así, dice el experto geofísico, no podría haber una atmósfera que encerrase el aire y los mares, que escaparían al espacio.
Es lo que ocurrió con Marte, recuerda, que perdió su campo magnético y con él sus condiciones habitables.</p>
<h3>Actividad tectónica</h3>
<p>El movimiento de las placas tectónicas y la de los movimientos sísmicos se explican solo con una Tierra redonda.
"Solo en una esfera encajan todas las placas de manera sensata", afirma Davis.

Los movimientos de las placas en un lado de la Tierra afectan los movimientos en el otro.

Las áreas de la Tierra que crean corteza, como la cordillera de los Andes, se contrarrestan con los lugares que consumen corteza, como las zonas de succión con grandes valles y fosas. Las cordilleras y muchos otros fenómenos geofísicos como los volcanes se producen por los movimientos de las placas.
En una Tierra plana, nada de esto podría explicarse adecuadamente.
No podría entenderse por qué existen montañas, cordilleras o terremotos.
También tendría que haber una explicación de lo que sucede con las placas en el borde del mundo.
Uno podría imaginarse que pueden caerse, pero los "terraplanistas" ya han establecido que existe un "muro de hielo" en el borde creado por la Antártica, algo muy difícil de creer, opina Davis.
Por qué las placas tectónicas y sus movimientos son indispensables para la vida
Para concluir, desde el Observatorio Terrestre Lamont-Doherty aseguran que si viviésemos en una Tierra plana no tendríamos ninguna duda porque todo sería radicalmente distinto a lo que conocemos hoy (si fuese posible).</p>
